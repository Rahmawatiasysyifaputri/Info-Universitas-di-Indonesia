			<p style="margin:20px;">Daftar universitas negeri di Indonesia - Secara umum ada 2 jenis perguruan tinggi/universitas, 
			   yakni universitas negeri dan universitas swasta. Dalam artikel ini akan dibahas mengenai daftar
			   PTN di Indonesia yang masuk dalam list seleksi SNMPTN untuk masuk perguruan tinggi, baik yang berbentuk universitas, institut, akademi atau universitas Islam.
			   Perguruan tinggi adalah jenjang pendidikan tinggi usai lulus dari SMA/SMK/sederajat. Perguruan 
			   tinggi bisa berupa universitas, institut, akademi, sekolah tinggi, universitas Islam dan lain-lain. 
			   Ada 2 tipe perguruan tinggi yakni perguruan tinggi negeri (PTN) dan perguruan tinggi swasta.
           <br>
            <br>
			   	 PTN adalah perguruan tinggi yang dikelola oleh pemerintah. Banyak PTN dan universitas ternama di 
			   Indonesia yang jadi favorit. Tentunya beberapa universitas terbaik di Indonesia jadi tujuan para 
			   calon mahasiswa untuk mendaftar.
            </br>

            <br>
			    Contoh universitas terfavorit di Indonesia antara lain misalnya yaitu Universitas Indonesia (UI), 
			   Institut Teknplogi Bandung (ITB), Universitas Gajah Mada (UGM), Institut Teknologi Sepuluh Nopember (ITS)
			   dan Universitas Airlangga (UNAIR).
			</br>

            <br>
			    Selain itu ada juga universitas negeri lain yang tak kalah terkenal. Dalam situs SNMPTN (seleksi nasional 
			   masuk perguruan tinggi negeri), ada total 85 daftar universitas negeri di Indonesia yang bisa dijadikan 
			   sasaran bagi calon mahasiswa untuk daftar universitas SNMPTN.
